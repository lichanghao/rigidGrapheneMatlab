function N = Bspline(v, w)
      N = zeros(12, 1);
      t1 = 1.D0-v-w;
      t2 = t1^2;
      t3 = t2^2;
      t4 = t2*t1;
      t5 = t4*v;
      t6 = 2.D0*t5;
      t8 = t4*w;
      t9 = 2.D0*t8;
      t11 = 6.D0*t5;
      t13 = t2*v*w;
      t14 = 6.D0*t13;
      t15 = v^2;
      t16 = t2*t15;
      t17 = 12.D0*t16;
      t19 = t1*t15*w;
      t20 = 6.D0*t19;
      t21 = t15*v;
      t22 = t1*t21;
      t23 = 6.D0*t22;
      t24 = t21*w;
      t25 = 2.D0*t24;
      t26 = t15^2;
      t30 = w^2;
      t31 = t2*t30;
      t32 = 24.D0*t31;
      t33 = t30*w;
      t34 = t1*t33;
      t36 = t30^2;
      t40 = t1*v*t30;
      t41 = 36.D0*t40;
      t42 = v*t33;
      t43 = 6.D0*t42;
      t44 = 24.D0*t16;
      t45 = 36.D0*t19;
      t46 = t15*t30;
      t47 = 12.D0*t46;
      t49 = 6.D0*t24;
      t50 = 6.D0*t3+24.D0*t8+t32+8.D0*t34+t36+24.D0*t5+60.D0*t13+t41+t43 ...
           +t44+t45+t47+8.D0*t22+t49+t26;
      t51 = 6.D0*t8;
      t52 = 12.D0*t31;
      t53 = 6.D0*t34;
      t54 = 6.D0*t40;
      t55 = 2.D0*t42;
      t57 = 2.D0*t22;
      t60 = 36.D0*t13;
      t63 = 24.D0*t46;
      t67 = t3+t51+t52+t53+t36+8.D0*t5+t60+t41+8.D0*t42+t44+60.D0*t19+ ...
	        t63+24.D0*t22+24.D0*t24+6.D0*t26;
      t74 = t3+8.D0*t8+t32+24.D0*t34+6.D0*t36+t11+t60+60.D0*t40+24.D0*t42 ...
	        +t17+t45+t63+t23+8.D0*t24+t26;
      t75 = 2.D0*t34;
      
      N(1) = t3+t6;
      N(2) = t3+t9;
      N(3) = t3+t9+t11+t14+t17+t20+t23+t25+t26;
      N(4) = t50;
      N(5) = t3+t51+t52+t53+t36+t6+t14+t54+t55;
      N(6) = t57+t26;
      N(7) = t67;
      N(8) = t74;
      N(9) = t75+t36;
      N(10) = t25+t26;
      N(11) = t75+t36+t54+t43+t20+t47+t57+t49+t26;
      N(12) = t36+t55;

      N = N./12;